/// Represents a set within a workout.
///
/// The [kilo] property represents the weight in kilograms for this set.
/// The [reps] property represents the number of repetitions for this set.
///
/// authors: Jackie, Christoffer & Jakob
class WorkoutSet {
  int kilo;
  int reps;

  WorkoutSet({required this.kilo, required this.reps});
}
